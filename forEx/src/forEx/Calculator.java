package forEx;

public class Calculator {


		//접근제한자,반환타입, 메서드명
		//두개의 정수를 받아서 두수의합을 반환합니다.
		public int add1(int num1, int num2){
		
		return num1+num2;

		}
		public int add2(int num3, int num4){
			
		return num3-num4;

		}
		public double add3(double num5, double num6){
			
		return num5*num6;

		}
		public double add4(double num7, double num8){
			
		return num7/num8;

		}
		public int add5(int num9, int num10){
			
		return num9%num10;

		}
}
